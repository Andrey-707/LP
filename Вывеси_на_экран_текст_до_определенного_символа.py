# Способ №3. То же самое, только через отрицание.
'''
В переменную sharp_index запишется индекс символа решетки, если он будет найден
методом find. Если символа решетки в строке не будет, в переменную sharp_index
будет записано значение -1'''

print("[bold magenta]Program start[/]")

# Печатаем строку, в которой в любом месте указываем символ, до которого нужно будет выводить
