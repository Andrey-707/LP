# print(string) # раскоментировать для просмотра

# в sharp_index сохраняется число (индекс) символа "#"
sharp_index = string.find("#")
# print(sharp_index) # раскоментировать для просмотра

# Если 'sharp_index' не равен -1 (символ "#" есть в строке), то печатай срез текста от 0 до 'sharp_index'
