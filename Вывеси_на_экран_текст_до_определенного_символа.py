# Вывеси на экран текст до определенного символа.
from rich import print


# Способ №1. Срез строки от индекса 0 до индекса символа.
# Метод строки str.find(sub[, start[, end]])
# sub : Подстрока, начальный индекс размещения которой требуется определить.
# start=0 : Индекс начала среза в исходной строке, в котором требуется отыскать подстроку.
# end=None : Индекс конца среза в исходной строке, в котором требуется отыскать подстроку.

print("[bold magenta]Program start[/]")

# Печатаем строку, в которой в любом месте указываем символ, до которого нужно будет выводить
# строку на печать. Этот символ далее введем в substring.
# string = input("Введите строку: ") # раскоментировать для ручного ввода
string = "Андрей#Андреев"
# print(string) # раскоментировать для просмотра

# введем в substring какой-нибудь символ. Например, "#"
# substring = input("Введите подстроку: ") # раскоментировать для ручного ввода
substring = "#"
# print(substring) # раскоментировать для просмотра

# длина строки
# print(len(string)) # раскоментировать для просмотра

# количетво символов до символа substring
# print(string.find(substring, 0, len(string))) # раскоментировать для просмотра

# если символ 'substring' есть в строке 'string', то вывеси на экран текст от индекса 0 до
# определенного символа, указанного в 'substring'
if substring in string:
    print(string[0:string.find(substring, 0, len(string))])
else:
    print(string)

print("[bold magenta]Program finish[/]")

print() # пустая строка

