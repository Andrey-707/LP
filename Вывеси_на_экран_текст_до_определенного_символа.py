# Способ №2. Тот же срез строки от индекса 0 до индекса символа.
# В переменную sharp_index запишется индекс символа решетки, если он будет найден.

print("[bold magenta]Program start[/]")

# Печатаем строку, в которой в любом месте указываем символ, до которого нужно будет выводить
# строку на печать.
# string = input("Введите строку: ") # раскоментировать для ручного ввода
string = "Андрей#Андреев"
# print(string) # раскоментировать для просмотра

# в sharp_index сохраняется число (индекс) символа "#"
sharp_index = string.find("#")
# print(sharp_index) # раскоментировать для просмотра

# если символ "#" есть в строке 'string', то вывеси на экран текст от индекса 0 до 'sharp_index'
if "#" in string:
    print(string[0:sharp_index])
else:
    print(string)

print("[bold magenta]Program finish[/]")

print() # пустая строка

